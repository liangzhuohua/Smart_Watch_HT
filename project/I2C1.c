//-----------------------------------------------------------------------------
void I2C1_check_lock(void)
{
  u32 ct;
  for(ct=0; ct<100; ct++) //waiting for stop end
  {
    if(!(HT_I2C1->CR & 0x02))
      break;
    else
      clk_delay(100);
  }
  if(GPIO_ReadInBit(I2C1_SDA_GPIO_PORT, I2C1_SDA_AFIO_PIN) == RESET)
    I2C1_unlock();
}

